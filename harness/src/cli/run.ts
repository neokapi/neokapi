/**
 * Harness orchestrator.
 *
 *   npm run demo <id|all> [--only=capture,artifacts,narrate,render] [--force] [--quality=draft|final]
 *   npm run demo --list
 *
 * Stages run in order and each is idempotent (skips if its output exists unless --force).
 */
import fs from "node:fs";
import path from "node:path";
import { loadEnv } from "../lib/env.ts";
import { listDemoIds, loadManifest } from "../lib/manifest.ts";
import { ASSETS_DIR, PUBLIC_DIR, ensureDir, publicDemoDir } from "../lib/paths.ts";
import { captureDemo, renormalizeDemo } from "../driver/capture.ts";
import { captureScript } from "../driver/capture-script.ts";
import { captureDesktopDemo } from "../driver/capture-desktop.ts";
import { captureArtifacts } from "../driver/artifacts.ts";
import { narrateDemo } from "../narrate/synth.ts";
import { renderDemo, type ThemeMode } from "../remotion/render.ts";
import { publishDemo } from "../remotion/publish.ts";

type Stage = "capture" | "normalize" | "artifacts" | "narrate" | "render" | "publish";
// Publish (mp4 → docs webm) runs last and is a no-op for demos without publishAs.
const ALL_STAGES: Stage[] = ["capture", "artifacts", "narrate", "render", "publish"];

function parseThemes(v: string): ThemeMode[] {
  if (v === "both" || v === "all") return ["dark", "light"];
  return v.split(",").map((s) => s.trim()).filter((s) => s === "light" || s === "dark") as ThemeMode[];
}

function parseArgs(argv: string[]) {
  const ids: string[] = [];
  let only: Stage[] = ALL_STAGES;
  let force = false;
  let list = false;
  let quality: "draft" | "final" = "final";
  // Docs ship theme-matched videos (light + dark), so render both by default.
  let themes: ThemeMode[] = ["dark", "light"];
  let docsDir: string | undefined;
  for (const a of argv) {
    if (a === "--list") list = true;
    else if (a === "--force") force = true;
    else if (a.startsWith("--only=")) only = a.slice(7).split(",").map((s) => s.trim()) as Stage[];
    else if (a.startsWith("--quality=")) quality = a.slice(10) as "draft" | "final";
    else if (a.startsWith("--theme=")) themes = parseThemes(a.slice(8));
    else if (a.startsWith("--docs-dir=")) docsDir = a.slice(11);
    else if (!a.startsWith("--")) ids.push(a);
  }
  if (themes.length === 0) themes = ["dark"];
  return { ids, only, force, list, quality, themes, docsDir };
}

/** Copy shared static assets (e.g. the mascot) into public/ so Remotion staticFile() finds them. */
function stageAssets(): void {
  ensureDir(PUBLIC_DIR);
  if (!fs.existsSync(ASSETS_DIR)) return;
  for (const f of fs.readdirSync(ASSETS_DIR)) {
    if (/\.(png|jpg|jpeg|webp|svg)$/i.test(f)) fs.copyFileSync(path.join(ASSETS_DIR, f), path.join(PUBLIC_DIR, f));
  }
}

/** Regenerate the Remotion registry from demos that have a capture.json. */
function writeRegistry(): void {
  ensureDir(PUBLIC_DIR);
  const entries = listDemoIds()
    .filter((id) => fs.existsSync(path.join(publicDemoDir(id), "capture.json")))
    .map((id) => {
      const cap = JSON.parse(fs.readFileSync(path.join(publicDemoDir(id), "capture.json"), "utf8"));
      return { id, title: cap.title as string };
    });
  const ts = `// Auto-generated by src/cli/run.ts. Do not edit by hand.
export interface RegistryEntry { id: string; title: string; }
export const DEMOS: RegistryEntry[] = ${JSON.stringify(entries, null, 2)};
`;
  fs.writeFileSync(path.join(import.meta.dirname!, "..", "remotion", "registry.generated.ts"), ts);
  fs.writeFileSync(path.join(PUBLIC_DIR, "registry.json"), JSON.stringify(entries, null, 2));
}

async function main() {
  loadEnv();
  const { ids, only, force, list, quality, themes, docsDir } = parseArgs(process.argv.slice(2));
  const available = listDemoIds();

  if (list || ids.length === 0) {
    console.log(`Demos (${available.length}):`);
    for (const id of available) {
      const m = loadManifest(id);
      console.log(`  ${id.padEnd(34)} ${m.title}`);
    }
    if (ids.length === 0 && !list) console.log(`\nUsage: npm run demo <id|all> [--only=...] [--force]`);
    return;
  }

  // "all" → every demo; "published" → only demos with publishAs (the docs videos).
  const selected = ids.includes("all")
    ? available
    : ids.includes("published")
      ? available.filter((id) => loadManifest(id).publishAs)
      : ids;
  const targets = selected.filter((id) => {
    if (available.includes(id)) return true;
    console.error(`Unknown demo: ${id} (have: ${available.join(", ")})`);
    return false;
  });
  const manifests = targets.map(loadManifest);

  // Stages run across ALL demos before advancing. This matters for render: the
  // Remotion bundle snapshots public/ at bundle time, so every demo's narration +
  // artifacts must be finalized before the first render bundles them.
  for (const m of manifests) {
    if (only.includes("capture")) {
      console.log(`\n━━ capture · ${m.id} ━━`);
      // Desktop demos record a Kapi Desktop UI screencast; scripted shell demos
      // record real command output (no Claude); Claude demos run a live session.
      if (m.terminal === "desktop") await captureDesktopDemo(m, { force });
      else if (m.terminal === "shell" || (m.script?.length ?? 0) > 0) await captureScript(m, { force });
      else await captureDemo(m, { force });
    }
  }
  for (const m of manifests) {
    if (only.includes("normalize")) {
      console.log(`\n━━ normalize · ${m.id} ━━`);
      renormalizeDemo(m);
    }
  }
  for (const m of manifests) {
    if (only.includes("artifacts")) {
      console.log(`\n━━ artifacts · ${m.id} ━━`);
      await captureArtifacts(m, { force });
    }
  }
  for (const m of manifests) {
    if (only.includes("narrate")) {
      console.log(`\n━━ narrate · ${m.id} ━━`);
      await narrateDemo(m, { force });
    }
  }
  writeRegistry();
  if (only.includes("render")) stageAssets();
  for (const m of manifests) {
    if (only.includes("render")) {
      console.log(`\n━━ render · ${m.id} (${themes.join(", ")}) ━━`);
      for (const themeMode of themes) {
        await renderDemo(m.id, { force, quality, themeMode });
      }
    }
  }
  for (const m of manifests) {
    if (only.includes("publish") && m.publishAs) {
      console.log(`\n━━ publish · ${m.id} → ${m.publishAs} ━━`);
      await publishDemo(m, { docsDir });
    }
  }
  console.log("\nDone.");
}

main().catch((e) => {
  console.error("\nharness error:", e?.stack || e?.message || e);
  process.exit(1);
});
