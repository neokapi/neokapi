/**
 * @neokapi/kapi-react runtime — thin translation layer for OTA mode.
 *
 * ~2KB total. Only loaded when mode='runtime'. Inline mode needs no runtime at all.
 *
 * Features:
 *   - t(hash, fallback, params?) — string translation with ICU support
 *   - tx(hash, fallback, elements?, params?) — rich JSX translation (inline elements)
 *   - setTranslations() / loadTranslations() — update dictionary, trigger re-renders
 *   - useNeokapi() — React hook for reactive translation updates
 */

import type { ReactNode } from 'react';
import { createElement, useSyncExternalStore, useCallback } from 'react';
import { resolveICU } from './icu.ts';

// ─── Translation store ───────────────────────────────────────

let currentLocale = '';
let dict: Record<string, string> = {};
let version = 0;
const listeners = new Set<() => void>();

function notify() {
  version++;
  listeners.forEach((fn) => fn());
}

/**
 * Set the active translation dictionary. Triggers re-render of all
 * components using useNeokapi().
 */
export function setTranslations(
  locale: string,
  translations: Record<string, string>,
) {
  currentLocale = locale;
  dict = translations;
  notify();
}

/**
 * Fetch a translation file from a URL and activate it.
 */
export async function loadTranslations(
  locale: string,
  url: string,
): Promise<void> {
  const response = await fetch(url);
  const translations = await response.json();
  setTranslations(locale, translations);
}

// ─── String translation ──────────────────────────────────────

/**
 * Look up a translation by hash. Falls back to source text.
 * Returns a string — use tx() for translations with inline JSX elements.
 *
 * Generated by the plugin:
 *   t("3kF", "Welcome, {name}!", { name })
 */
export function t(
  hash: string,
  fallback: string,
  params?: Record<string, string | number>,
): string {
  let text = dict[hash] ?? fallback;

  // Resolve ICU plural/select if present
  if (text.includes(', plural,') || text.includes(', select,')) {
    text = resolveICU(text, params, currentLocale);
  }

  // Substitute {param} tokens
  if (params) {
    for (const [key, value] of Object.entries(params)) {
      text = text.replaceAll(`{${key}}`, String(value));
    }
  }

  return text;
}

// ─── Rich JSX translation ────────────────────────────────────

/**
 * Look up a translation by hash, with support for inline JSX elements.
 * Returns ReactNode — string segments interleaved with React elements.
 *
 * Generated by the plugin for text containing inline elements like <a>, <strong>:
 *   tx("9qR", "Click {=m0} to continue.", { "=m0": <a href="/x">here</a> })
 *
 * The elements map provides the original JSX elements keyed by their
 * token names (=m0, =m1, etc.). The translated text can reorder these
 * tokens freely — e.g., German might produce "Klicken Sie {=m0}, um fortzufahren."
 */
export function tx(
  hash: string,
  fallback: string,
  elements: Record<string, ReactNode>,
  params?: Record<string, string | number>,
): ReactNode {
  let text = dict[hash] ?? fallback;

  // Resolve ICU
  if (text.includes(', plural,') || text.includes(', select,')) {
    text = resolveICU(text, params, currentLocale);
  }

  // Substitute string params first (not element tokens)
  if (params) {
    for (const [key, value] of Object.entries(params)) {
      text = text.replaceAll(`{${key}}`, String(value));
    }
  }

  // Split on element tokens {=m0}, {=m1}, etc. and interleave with elements
  const parts: ReactNode[] = [];
  const tokenRegex = /\{(=[^}]+)\}/g;
  let lastIndex = 0;
  let match;
  let hasElements = false;

  while ((match = tokenRegex.exec(text)) !== null) {
    if (match.index > lastIndex) {
      parts.push(text.slice(lastIndex, match.index));
    }
    const tokenName = match[1];
    if (elements[tokenName] !== undefined) {
      parts.push(elements[tokenName]);
      hasElements = true;
    } else {
      parts.push(match[0]);
    }
    lastIndex = match.index + match[0].length;
  }

  if (lastIndex < text.length) {
    parts.push(text.slice(lastIndex));
  }

  if (!hasElements) {
    return parts.join('');
  }

  // Return a fragment with keyed children
  return createElement(
    'span',
    { 'data-neokapi': '' },
    ...parts.map((part, i) =>
      typeof part === 'string' ? part : createElement('span', { key: i }, part),
    ),
  );
}

// ─── React hook ──────────────────────────────────────────────

/**
 * React hook for reactive translations. Re-renders when translations change.
 */
export function useNeokapi() {
  const subscribe = useCallback((callback: () => void) => {
    listeners.add(callback);
    return () => {
      listeners.delete(callback);
    };
  }, []);

  const getSnapshot = useCallback(() => version, []);

  useSyncExternalStore(subscribe, getSnapshot, getSnapshot);

  return {
    t,
    tx,
    locale: currentLocale,
    setTranslations,
    loadTranslations,
  };
}
