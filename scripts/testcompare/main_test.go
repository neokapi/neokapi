func TestMerge_ParameterizedJUnitTests(t *testing.T) {
	// parseSurefire deduplicates parameterized JUnit tests (e.g. "testFoo[0: ...]")
	// into a single entry with the base method name. The Okapi data passed to
	// merge already has deduplicated names, so merge sees "testFoo" once.
	okapi := map[string]*FilterResult{
		"html": {
			Suites: []Suite{{
				Name: "HtmlSnippetsTest",
				Tests: []Test{
					{Name: "testFoo", ClassName: "net.sf.okapi.filters.html.HtmlSnippetsTest", Status: "pass"},
					{Name: "testBar", ClassName: "net.sf.okapi.filters.html.HtmlSnippetsTest", Status: "pass"},
				},
				Total: 2, Passed: 2,
			}},
			Total: 2, Passed: 2,
		},
	}

	bridgeAnns := []annotation{
		{JavaClass: "HtmlSnippetsTest", JavaMethod: "testFoo", GoTest: "TestSnippets_Foo", Filter: "html", File: "test.go", Line: 10},
		{JavaClass: "HtmlSnippetsTest", JavaMethod: "testBar", GoTest: "TestSnippets_Bar", Filter: "html", File: "test.go", Line: 20},
	}

	bridgeStatus := map[string]string{
		"html/TestSnippets_Foo": "pass",
		"html/TestSnippets_Bar": "pass",
	}

	data := merge(okapi, nil, nil,
		bridgeAnns, nil,
		nil, nil,
		bridgeStatus, nil,
		nil, nil,
		nil, nil,
		"", "", "", "")

	require.Len(t, data.Filters, 1)
	tcs := data.Filters[0].TestCases

	// Both test cases should be mapped
	require.Len(t, tcs, 2)
	for _, tc := range tcs {
		assert.NotEmpty(t, tc.BridgeTest, "test %s should be mapped", tc.JavaMethod)
		assert.Equal(t, "implemented", tc.TestState, "test %s should be implemented", tc.JavaMethod)
	}

	fooCase := findTCM(tcs, "testFoo")
	require.NotNil(t, fooCase)
	assert.Equal(t, "TestSnippets_Foo", fooCase.BridgeTest)

	barCase := findTCM(tcs, "testBar")
	require.NotNil(t, barCase)
	assert.Equal(t, "TestSnippets_Bar", barCase.BridgeTest)
}

func TestParseSurefire_DeduplicatesParameterized(t *testing.T) {
	// Create a Surefire XML with parameterized test cases.
	dir := t.TempDir()
	filterDir := filepath.Join(dir, "html")
	require.NoError(t, os.MkdirAll(filterDir, 0o755))

	xml := `<?xml version="1.0" encoding="UTF-8"?>
<testsuite name="net.sf.okapi.filters.html.HtmlSnippetsTest" tests="4">
  <testcase name="testFoo[0: case0]" classname="net.sf.okapi.filters.html.HtmlSnippetsTest" time="0.01"/>
  <testcase name="testFoo[1: case1]" classname="net.sf.okapi.filters.html.HtmlSnippetsTest" time="0.02"/>
  <testcase name="testFoo[2: case2]" classname="net.sf.okapi.filters.html.HtmlSnippetsTest" time="0.03">
    <failure message="boom"/>
  </testcase>
  <testcase name="testBar" classname="net.sf.okapi.filters.html.HtmlSnippetsTest" time="0.05"/>
</testsuite>`
	require.NoError(t, os.WriteFile(filepath.Join(filterDir, "TEST-net.sf.okapi.filters.html.HtmlSnippetsTest.xml"), []byte(xml), 0o644))

	result := parseSurefire(dir)
	require.Contains(t, result, "html")

	fr := result["html"]
	require.Len(t, fr.Suites, 1)
	suite := fr.Suites[0]

	// 3 parameterized instances of testFoo should collapse into 1 entry
	assert.Equal(t, 2, suite.Total, "should have 2 tests after dedup")
	assert.Equal(t, 2, fr.Total)

	// testFoo should exist with base name and worst status (fail > pass)
	fooTest := findTest(suite.Tests, "testFoo")
	require.NotNil(t, fooTest, "testFoo should exist as deduped entry")
	assert.Equal(t, "fail", fooTest.Status, "should keep worst status")

	// testBar should be unchanged
	barTest := findTest(suite.Tests, "testBar")
	require.NotNil(t, barTest)
	assert.Equal(t, "pass", barTest.Status)

	// Counts should reflect deduped entries
	assert.Equal(t, 1, suite.Passed)
	assert.Equal(t, 1, suite.Failed)
}

func findTCM(tcs []TestCaseMatch, method string) *TestCaseMatch {
	for i := range tcs {
		if tcs[i].JavaMethod == method {
			return &tcs[i]
		}
	}
	return nil
}

func TestParseGoTestResults_SubtestGrouping(t *testing.T) {
	dir := t.TempDir()
	jsonl := filepath.Join(dir, "bridge.jsonl")

	lines := []string{
		`{"Action":"pass","Package":"github.com/gokapi/gokapi/core/plugin/bridge/filters/html","Test":"TestFoo","Elapsed":0.1}`,
		`{"Action":"pass","Package":"github.com/gokapi/gokapi/core/plugin/bridge/filters/html","Test":"TestFoo/sub1","Elapsed":0.01}`,
		`{"Action":"pass","Package":"github.com/gokapi/gokapi/core/plugin/bridge/filters/html","Test":"TestFoo/sub2","Elapsed":0.02}`,
		`{"Action":"pass","Package":"github.com/gokapi/gokapi/core/plugin/bridge/filters/html","Test":"TestBar","Elapsed":0.05}`,
	}
	require.NoError(t, os.WriteFile(jsonl, []byte(strings.Join(lines, "\n")), 0o644))

	results := parseGoTestResults(jsonl, bridgeFilterFromPkg)

	require.Contains(t, results.filters, "html")
	fr := results.filters["html"]

	// Total includes all tests + subtests
	assert.Equal(t, 4, fr.Total)
	// Funcs counts only top-level test functions (no "/" in name)
	assert.Equal(t, 2, fr.Funcs)
	// Suite-level Funcs
	assert.Equal(t, 2, fr.Suites[0].Funcs)

	// Subtest counts
	require.Contains(t, results.subtestCounts, "html")
	assert.Equal(t, 2, results.subtestCounts["html"]["TestFoo"])
	assert.Equal(t, 0, results.subtestCounts["html"]["TestBar"])
}

func TestMerge_UnmappedDoesNotOverrideBridgeAnnotation(t *testing.T) {
	// A native okapi-unmapped: should not hide an existing bridge okapi: annotation.
	okapi := map[string]*FilterResult{
		"html": {
			Suites: []Suite{{
				Name: "HtmlConfigurationSupportTest",
				Tests: []Test{
					{Name: "test_EXCLUDE", ClassName: "net.sf.okapi.filters.html.HtmlConfigurationSupportTest", Status: "pass"},
					{Name: "test_INCLUDE", ClassName: "net.sf.okapi.filters.html.HtmlConfigurationSupportTest", Status: "pass"},
				},
				Total: 2, Passed: 2,
			}},
			Total: 2, Passed: 2,
		},
	}

	// Bridge has annotations for both tests
	bridgeAnns := []annotation{
		{JavaClass: "HtmlConfigurationSupportTest", JavaMethod: "test_EXCLUDE", GoTest: "TestConfigSupport_Exclude", Filter: "html", File: "config_test.go", Line: 85},
		{JavaClass: "HtmlConfigurationSupportTest", JavaMethod: "test_INCLUDE", GoTest: "TestConfigSupport_Include", Filter: "html", File: "config_test.go", Line: 108},
	}

	// Native has okapi-unmapped for both — should not override bridge implementation
	nativeUnmapped := []skipAnnotation{
		{JavaClass: "HtmlConfigurationSupportTest", JavaMethod: "test_EXCLUDE", Reason: "Java YAML config subsystem", Filter: "html"},
		{JavaClass: "HtmlConfigurationSupportTest", JavaMethod: "test_INCLUDE", Reason: "Java YAML config subsystem", Filter: "html"},
	}

	bridgeStatus := map[string]string{
		"html/TestConfigSupport_Exclude": "pass",
		"html/TestConfigSupport_Include": "pass",
	}

	data := merge(okapi, nil, nil,
		bridgeAnns, nil,
		nil, nativeUnmapped,
		bridgeStatus, nil,
		nil, nil,
		nil, nil,
		"", "", "", "")

	require.Len(t, data.Filters, 1)
	tcs := data.Filters[0].TestCases
	require.Len(t, tcs, 2)

	for _, tc := range tcs {
		assert.NotEmpty(t, tc.BridgeTest, "test %s bridge should be mapped", tc.JavaMethod)
		assert.Equal(t, "pass", tc.BridgeStatus, "test %s bridge should pass", tc.JavaMethod)
		assert.Equal(t, "implemented", tc.TestState, "test %s should be implemented, not not-applicable", tc.JavaMethod)
	}
}

