func TestParseFileAnnotations(t *testing.T) {
	dir := t.TempDir()

	// Create a mock bridge test file
	bridgeDir := filepath.Join(dir, "okf_html")
	require.NoError(t, os.MkdirAll(bridgeDir, 0o755))

	content := `package okf_html

import "testing"

// okapi: HtmlSnippetsTest#testHref
func TestExtract_Href(t *testing.T) {
	// test body
}

// okapi: HtmlSnippetsTest#testBoldTag
// okapi: HtmlSnippetsTest#testItalicTag
func TestExtract_InlineCodes(t *testing.T) {
	// test body
}

func TestSomethingWithoutAnnotation(t *testing.T) {
	// no okapi annotation
}
`
	testFile := filepath.Join(bridgeDir, "html_test.go")
	require.NoError(t, os.WriteFile(testFile, []byte(content), 0o644))

	ar := parseFileAnnotations(testFile, "bridge")

	assert.Len(t, ar.annotations, 3)

	// First annotation
	assert.Equal(t, "HtmlSnippetsTest", ar.annotations[0].JavaClass)
	assert.Equal(t, "testHref", ar.annotations[0].JavaMethod)
	assert.Equal(t, "TestExtract_Href", ar.annotations[0].GoTest)
	assert.Equal(t, "html", ar.annotations[0].Filter)

	// Multi-annotation (two annotations → same Go test)
	assert.Equal(t, "HtmlSnippetsTest", ar.annotations[1].JavaClass)
	assert.Equal(t, "testBoldTag", ar.annotations[1].JavaMethod)
	assert.Equal(t, "TestExtract_InlineCodes", ar.annotations[1].GoTest)

	assert.Equal(t, "HtmlSnippetsTest", ar.annotations[2].JavaClass)
	assert.Equal(t, "testItalicTag", ar.annotations[2].JavaMethod)
	assert.Equal(t, "TestExtract_InlineCodes", ar.annotations[2].GoTest)
}

