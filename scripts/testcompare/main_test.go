func TestMerge_ParameterizedJUnitTests(t *testing.T) {
	// Surefire XML reports parameterized JUnit tests with [N: ...] suffixes.
	// The merge function should match these to annotations that use the
	// unparameterized base method name.
	okapi := map[string]*FilterResult{
		"html": {
			Suites: []Suite{{
				Name: "HtmlSnippetsTest",
				Tests: []Test{
					{Name: "testFoo[0: case0]", ClassName: "net.sf.okapi.filters.html.HtmlSnippetsTest", Status: "pass"},
					{Name: "testFoo[1: case1]", ClassName: "net.sf.okapi.filters.html.HtmlSnippetsTest", Status: "pass"},
					{Name: "testBar", ClassName: "net.sf.okapi.filters.html.HtmlSnippetsTest", Status: "pass"},
				},
				Total: 3, Passed: 3,
			}},
			Total: 3, Passed: 3,
		},
	}

	bridgeAnns := []annotation{
		{JavaClass: "HtmlSnippetsTest", JavaMethod: "testFoo", GoTest: "TestSnippets_Foo", Filter: "html", File: "test.go", Line: 10},
		{JavaClass: "HtmlSnippetsTest", JavaMethod: "testBar", GoTest: "TestSnippets_Bar", Filter: "html", File: "test.go", Line: 20},
	}

	bridgeStatus := map[string]string{
		"html/TestSnippets_Foo": "pass",
		"html/TestSnippets_Bar": "pass",
	}

	data := merge(okapi, nil, nil,
		bridgeAnns, nil,
		nil, nil,
		nil, nil,
		bridgeStatus, nil,
		nil, nil,
		nil, nil,
		"", "", "", "")

	require.Len(t, data.Filters, 1)
	tcs := data.Filters[0].TestCases

	// All 3 test cases should be mapped (2 parameterized + 1 regular)
	for _, tc := range tcs {
		assert.NotEmpty(t, tc.BridgeTest, "test %s should be mapped", tc.JavaMethod)
		assert.Equal(t, "implemented", tc.TestState, "test %s should be implemented", tc.JavaMethod)
	}

	// Parameterized cases should map to the same Go test
	fooCase0 := findTCM(tcs, "testFoo[0: case0]")
	fooCase1 := findTCM(tcs, "testFoo[1: case1]")
	require.NotNil(t, fooCase0)
	require.NotNil(t, fooCase1)
	assert.Equal(t, "TestSnippets_Foo", fooCase0.BridgeTest)
	assert.Equal(t, "TestSnippets_Foo", fooCase1.BridgeTest)

	// Regular case should map normally
	barCase := findTCM(tcs, "testBar")
	require.NotNil(t, barCase)
	assert.Equal(t, "TestSnippets_Bar", barCase.BridgeTest)
}

func findTCM(tcs []TestCaseMatch, method string) *TestCaseMatch {
	for i := range tcs {
		if tcs[i].JavaMethod == method {
			return &tcs[i]
		}
	}
	return nil
}

func TestParseGoTestResults_SubtestGrouping(t *testing.T) {
	dir := t.TempDir()
	jsonl := filepath.Join(dir, "bridge.jsonl")

	lines := []string{
		`{"Action":"pass","Package":"github.com/gokapi/gokapi/core/plugin/bridge/filters/okf_html","Test":"TestFoo","Elapsed":0.1}`,
		`{"Action":"pass","Package":"github.com/gokapi/gokapi/core/plugin/bridge/filters/okf_html","Test":"TestFoo/sub1","Elapsed":0.01}`,
		`{"Action":"pass","Package":"github.com/gokapi/gokapi/core/plugin/bridge/filters/okf_html","Test":"TestFoo/sub2","Elapsed":0.02}`,
		`{"Action":"pass","Package":"github.com/gokapi/gokapi/core/plugin/bridge/filters/okf_html","Test":"TestBar","Elapsed":0.05}`,
	}
	require.NoError(t, os.WriteFile(jsonl, []byte(strings.Join(lines, "\n")), 0o644))

	results := parseGoTestResults(jsonl, bridgeFilterFromPkg)

	require.Contains(t, results.filters, "html")
	fr := results.filters["html"]

	// Total includes all tests + subtests
	assert.Equal(t, 4, fr.Total)
	// Funcs counts only top-level test functions (no "/" in name)
	assert.Equal(t, 2, fr.Funcs)
	// Suite-level Funcs
	assert.Equal(t, 2, fr.Suites[0].Funcs)

	// Subtest counts
	require.Contains(t, results.subtestCounts, "html")
	assert.Equal(t, 2, results.subtestCounts["html"]["TestFoo"])
	assert.Equal(t, 0, results.subtestCounts["html"]["TestBar"])
}

func TestMerge_SkipDoesNotOverrideBridgeAnnotation(t *testing.T) {
	// A native okapi-skip: should not hide an existing bridge okapi: annotation.
	// This was the bug causing HtmlConfigurationSupportTest to show "—" for bridge.
	okapi := map[string]*FilterResult{
		"html": {
			Suites: []Suite{{
				Name: "HtmlConfigurationSupportTest",
				Tests: []Test{
					{Name: "test_EXCLUDE", ClassName: "net.sf.okapi.filters.html.HtmlConfigurationSupportTest", Status: "pass"},
					{Name: "test_INCLUDE", ClassName: "net.sf.okapi.filters.html.HtmlConfigurationSupportTest", Status: "pass"},
				},
				Total: 2, Passed: 2,
			}},
			Total: 2, Passed: 2,
		},
	}

	// Bridge has annotations for both tests
	bridgeAnns := []annotation{
		{JavaClass: "HtmlConfigurationSupportTest", JavaMethod: "test_EXCLUDE", GoTest: "TestConfigSupport_Exclude", Filter: "html", File: "config_test.go", Line: 85},
		{JavaClass: "HtmlConfigurationSupportTest", JavaMethod: "test_INCLUDE", GoTest: "TestConfigSupport_Include", Filter: "html", File: "config_test.go", Line: 108},
	}

	// Native has okapi-skip for both (the bug trigger)
	nativeSkips := []skipAnnotation{
		{JavaClass: "HtmlConfigurationSupportTest", JavaMethod: "test_EXCLUDE", Reason: "Java YAML config subsystem", Filter: "html"},
		{JavaClass: "HtmlConfigurationSupportTest", JavaMethod: "test_INCLUDE", Reason: "Java YAML config subsystem", Filter: "html"},
	}

	bridgeStatus := map[string]string{
		"html/TestConfigSupport_Exclude": "pass",
		"html/TestConfigSupport_Include": "pass",
	}

	data := merge(okapi, nil, nil,
		bridgeAnns, nil,
		nil, nativeSkips,
		nil, nil,
		bridgeStatus, nil,
		nil, nil,
		nil, nil,
		"", "", "", "")

	require.Len(t, data.Filters, 1)
	tcs := data.Filters[0].TestCases
	require.Len(t, tcs, 2)

	for _, tc := range tcs {
		assert.NotEmpty(t, tc.BridgeTest, "test %s bridge should be mapped", tc.JavaMethod)
		assert.Equal(t, "pass", tc.BridgeStatus, "test %s bridge should pass", tc.JavaMethod)
		assert.Equal(t, "implemented", tc.TestState, "test %s should be implemented, not skipped", tc.JavaMethod)
		// Skip reason preserved as context
		assert.Equal(t, "Java YAML config subsystem", tc.SkipReason)
	}
}

